/**
 * Converts raw OHLCV data to TradingView Lightweight Charts format
 */
export function formatOHLCVDataForChart() {
  return FET_OHLCV_DATA.map(([timestampMs, open, high, low, close, volume]) => {
    const timestampSeconds = Math.floor(timestampMs / 1000);
    const dateStr = new Date(timestampSeconds * 1000).toISOString().split('T')[0]!;
    
    return {
      time: dateStr,
      open: Number(open.toFixed(6)),
      high: Number(high.toFixed(6)),
      low: Number(low.toFixed(6)),
      close: Number(close.toFixed(6)),
      volume: Number(volume.toFixed(2))
    };
  });
}
